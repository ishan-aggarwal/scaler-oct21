	public static int majorityElement(final List<Integer> A) {

		Integer majorityElement = null;
		Integer freq = 0;

		for (int i = 0; i < A.size(); i++) {
			if (freq == 0) {
				majorityElement = A.get(i);
				freq = 1;
			} else if (A.get(i).compareTo(majorityElement) == 0) {
				freq++;
			} else {
				freq--;
			}
		}

//		return majorityElement;

		freq = 0;
		for (Integer ele : A) {
			if (ele.compareTo(majorityElement) == 0) {
				freq++;
			}
		}

		if (freq > A.size() / 2) {
			return majorityElement;
		} else {
			return -1;
		}

	}

